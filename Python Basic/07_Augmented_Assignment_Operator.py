# Augmented Assignment Operator

Some_value = 5
# Some_value = Some_value + 5
# instead of doing this we will use Augmented Assignment Operator,

Some_value += 5  # --> which is equal to Some_value = Some_value + 5

# NOTE : In order to work this "Some_value += 5", the variable "Some_value" should be defined before or in other words,
# It should have some value defined to it.

print(Some_value)

