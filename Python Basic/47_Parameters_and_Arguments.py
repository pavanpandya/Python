# Arguments Vs Parametes

# Parameters (Positional Parmeters) - They are used when we defined a function. eg: name and emoji
def say_hello(name, emoji):
    print(f'Hello {name} Good Morning {emoji}')


# Arguments (Positional Arguments)- Actual Value we provide to the funtion
# Calling the Function or Invoking or Invoke the Function
say_hello('Pavan', '<3')
say_hello('Deep', '<3')

# Positional Arguments: They are the Arguments that are required to be in proper positions.
